#include "libft.h"

int		ft_atoi_base(const char *str, int base)
{
	int	number;
	int	i;
	int negative;

	number = 0;
	i = 0;
	negative = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
	|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			negative++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number *= base;
		number += (str[i] - 48);
		i++;
	}
	if (negative == 1)
		return (-number);
	return (number);
}
